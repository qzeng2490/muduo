int howMuchTimeFromNow(Timestamp when)
{
  int64_t microseconds = when.microSecondsSinceEpoch()
                         - Timestamp::now().microSecondsSinceEpoch();

  if (microseconds < 1000)
  {
    microseconds = 1000;
  }
  return static_cast<int>(microseconds / 1000);
}

